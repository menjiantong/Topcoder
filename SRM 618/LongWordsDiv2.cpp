    {
        for(int j=i+1;j<s.length();++j)
        {
            for(int k=j+1;k<s.length();++k)
            {
                if(s[i]==s[k])
                {
                    for(int l=k+1;l<s.length();++l)
                    {
                        if(s[l]==s[j])f=0;
                    }
                }
            }
        }
    }

